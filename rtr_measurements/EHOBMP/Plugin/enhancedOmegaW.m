function [ y ] = enhancedOmegaW( x )
    if x < - 3.6
        y = 0.929404810843623 * exp( 0.986418416898303 * x ) - 6.79639452545093e-06;
    elseif x < 0
        y = - 0.00431200176082888 * exp( 3.55275697783407 * x ) + 0.00792228971991872 * x ^ 3 + 0.0907827580611564 * x ^ 2 + 0.375633000771424 * x + 0.571426071298747;
    elseif x < 2.5
        y = - 0.531567672565454 * exp( 0.267412600010990 * x ) - 0.00273225759962195 * x ^ 3 + 0.0953348873301105 * x ^ 2 + 0.503184890060992 * x + 1.09876994061414;
    elseif x < 7
        y = 1.32189881177891 * exp( - 2.05642141379680 * x ) - 0.00164391849625201 * x ^ 3 + 0.0428488786259462 * x ^ 2 + 0.481822411934234 * x + 0.418369998866986;
    elseif x < 30
        b = log( x + 0.217073722991741 );
        y = 1.00007068672705 * x - 1.00147317311501 * b + 1.08561409526715 * b / x + 0.516677184575301 * b * ( b - 2 ) / x ^ 2;
    else
        b = log( x );
        y = x - b + b / x + 0.5 * b * ( b - 2 ) / x ^ 2;
    end
    e = exp( x - y );
    f = y - e;
    f1 = 1 + e;
    y = y - f * ( 1 - 0.5 * ( f * e / f1 ) ^ 2 ) / f1;
end

